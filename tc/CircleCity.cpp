class CircleCity {
public:
    bool good(const vector<int>& dist, int k, int d) {
        int n = dist.size();
        vector<int> to(n), sum_to(n);
        for (int i = 0, j = 0, cur_sum = 0, cnt = 0; i < n; ++i) {
            while (cur_sum <= d && cnt < n) {
                cur_sum += dist[j];
                ++j, ++cnt;
                if (j == n) j = 0;
            }
            to[i] = j;
            sum_to[i] = cnt;
            cur_sum -= dist[i];
            --cnt;
        }

        for (int i = 0; i < n; ++i) {
            int cnt = 0;
            int j = i;
            for (int t = 0; t < k; ++t) {
                cnt += sum_to[j];
                j = to[j];
            }
            if (cnt >= n) return true;
        }
        return false;
    }

	int findMin(vector<int> dist, int k) {
        int lb = -1, rb = 2e9;
        while (lb + 1 < rb) {
            int mb = (lb + rb) / 2;
            if (good(dist, k, mb)) {
                rb = mb;
            } else {
                lb = mb;
            }
        }
        return rb;
	}
};


